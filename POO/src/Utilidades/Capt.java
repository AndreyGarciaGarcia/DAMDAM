package Utilidades;

public class Capt {

	public int numero;
	public int duracion;

	public Capt(int pNumero, int pDuracion) {

		numero = pNumero;
		duracion = pDuracion;

	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public int getDuracion() {
		return duracion;
	}

	public void setDuracion(int duracion) {
		this.duracion = duracion;
	}
	
}
